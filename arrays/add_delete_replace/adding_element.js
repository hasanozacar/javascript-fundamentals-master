let arr = ["One","Two","Three","Four","Five"];
//push method add the element at end of array
console.log(arr.push("Six"));// 6
console.log(arr);// 6 [ 'One', 'Two', 'Three', 'Four', 'Five', 'Six' ]
//unshift method add the element at beginning of array
console.log(arr.unshift("Test"));// 7
console.log(arr);//[ 'Test', 'One', 'Two', 'Three', 'Four', 'Five', 'Six' ]